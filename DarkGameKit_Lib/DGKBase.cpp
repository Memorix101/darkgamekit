void DarkGameKit::CallOnce()
{
	for (static bool first = true; first; first = false) {
		DarkGDK();
		printCopy = printRef;
	}

	for (int i = 0; i < printCopy.size(); i++)
	{
		//printRef.insert(printRef.begin(), printCopy.begin(), printCopy.end());
		printRef[i] = printCopy[i];
	}

	//display all text objects
	for (auto ref : textRef)
	{
		DrawFont(ref.pos, ref.text, ref.fontSize);
	}

	//update position to enable scroll effect
	for (int b = 0; b < printRef.size(); b++)
	{
		if (printRef[b].pos.y > (window_height - (printFontHeight)))
		{
			//update the full vector
			for (int i = 0; i < printRef.size(); i++)
			{
				(printRef[i].pos.y) -= (printFontHeight);
			}

			fontScrolling = true;
		}
		else
		{
			fontScrolling = false;
		}

		//update printCopy
		for (int a = 0; a < printCopy.size(); a++)
		{
			printCopy[a].pos.x = printRef[a].pos.x;
			printCopy[a].pos.y = printRef[a].pos.y;
		}

		DrawPrint(printRef[b].pos, printRef[b].text);
	}
}

void DarkGameKit::Run()
{
	while (!quit) {
		BeginDrawing();
			ClearBackground(BLACK);

			// DrawSprite({0, 0}); //Test
			DrawTrailModeWatermark();
			CallOnce();
			LoopGDK();

			DrawFPS(10, 10);
		EndDrawing();
	}

	// Always be sure to clean up 
	CloseWindow(); // Close window and OpenGL context
}

